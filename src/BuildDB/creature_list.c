void newrec_creaturelist()
{
  ULong uniqueID;
  VoidHand rec;
  Ptr p;
  ULong size, offset;
  UInt index;
  Short i;
  ULong qty = MAX_CREATURES;
  ULong sze = sizeof(creature_type);

  uniqueID = REC_CREATURELIST;
  if (0 == DmFindRecordByID(MoriaDB, uniqueID, &index))
    DmRemoveRecord(MoriaDB, index);      // EX TERM IN ATE !
  index = DmNumRecords(MoriaDB);
  size = qty * sze;
  rec = DmNewRecord(MoriaDB, &index, size);
  p = MemHandleLock(rec);
  // print stuff to it.
  for (i = 0, offset = 0 ; i < qty; i++, offset += sze)
    DmWrite(p, offset, &(creature_list[i]), sze);
  MemPtrUnlock(p);
  DmReleaseRecord(MoriaDB, index, true);
  DmSetRecordInfo(MoriaDB, index, NULL, &uniqueID); // set its uid!

  uniqueID = REC_CLIST_NAMES;
  // qty remains the same
  if (0 == DmFindRecordByID(MoriaDB, uniqueID, &index))
    DmRemoveRecord(MoriaDB, index);      // EX TERM IN ATE !
  index = DmNumRecords(MoriaDB);
  size = creature_list[MAX_CREATURES-1].name_offset + 7; //7=strlen("Balrog")+1
  rec = DmNewRecord(MoriaDB, &index, size);
  p = MemHandleLock(rec);
  // print stuff to it.
  for (i = 0 ; i < qty; i++)
    DmStrCopy(p, creature_list[i].name_offset, creature_names[i]);
  MemPtrUnlock(p);
  DmReleaseRecord(MoriaDB, index, true);
  DmSetRecordInfo(MoriaDB, index, NULL, &uniqueID); // set its uid!

}
