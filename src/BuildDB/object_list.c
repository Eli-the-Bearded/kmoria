void newrec_objectlist()
{
  ULong uniqueID;
  VoidHand rec;
  Ptr p;
  ULong size, offset;
  UInt index;
  Short i;
  ULong qty = MAX_OBJECTS;
  ULong sze = sizeof(treasure_type);

  uniqueID = REC_OBJECTLIST;
  if (0 == DmFindRecordByID(MoriaDB, uniqueID, &index))
    DmRemoveRecord(MoriaDB, index);      // EX TERM IN ATE !
  index = DmNumRecords(MoriaDB);
  size = qty * sze;
  rec = DmNewRecord(MoriaDB, &index, size);
  p = MemHandleLock(rec);
  // print stuff to it.
  for (i = 0, offset = 0 ; i < qty; i++, offset += sze)
    DmWrite(p, offset, &(object_list[i]), sze);
  MemPtrUnlock(p);
  DmReleaseRecord(MoriaDB, index, true);
  DmSetRecordInfo(MoriaDB, index, NULL, &uniqueID); // set its uid!

  uniqueID = REC_OBJLIST_NAMES;
  // qty remains the same
  if (0 == DmFindRecordByID(MoriaDB, uniqueID, &index))
    DmRemoveRecord(MoriaDB, index);      // EX TERM IN ATE !
  index = DmNumRecords(MoriaDB);
  size = name_offset[MAX_OBJECTS-1] + 1; // last one is "" so only needs 1 char
  rec = DmNewRecord(MoriaDB, &index, size);
  p = MemHandleLock(rec);
  // print stuff to it.
  for (i = 0 ; i < qty; i++)
    DmStrCopy(p, name_offset[i], object_names[i]);
  MemPtrUnlock(p);
  DmReleaseRecord(MoriaDB, index, true);
  DmSetRecordInfo(MoriaDB, index, NULL, &uniqueID); // set its uid!

}

